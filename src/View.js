class View {
  static NEWLINE() {
    return '<br>'
  }
  
  static SPACE() {
    return '&nbsp;';
  }

  static STARTTABLE() {
    return '<table>'
  }
  static ENDTABLE() {
    return '</table>'
  }
  static STARTTR() {
    return '<tr>'
  }
  static ENDTR() {
    return '</tr>'
  }
  static STARTTD() {
    return '<td>'
  }
  static ENDTD() {
    return '</td>'
  }
}